package vo;

public class BookVO {
	private int idx, b_price;
	private String category, b_name, b_company, 
			b_image_s, b_image_l, b_content;
	
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getB_price() {
		return b_price;
	}
	public void setB_price(int b_price) {
		this.b_price = b_price;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getB_name() {
		return b_name;
	}
	public void setB_name(String b_name) {
		this.b_name = b_name;
	}
	public String getB_company() {
		return b_company;
	}
	public void setB_company(String b_company) {
		this.b_company = b_company;
	}
	public String getB_image_s() {
		return b_image_s;
	}
	public void setB_image_s(String b_image_s) {
		this.b_image_s = b_image_s;
	}
	public String getB_image_l() {
		return b_image_l;
	}
	public void setB_image_l(String b_image_l) {
		this.b_image_l = b_image_l;
	}
	public String getB_content() {
		return b_content;
	}
	public void setB_content(String b_content) {
		this.b_content = b_content;
	}
	
	
	
}
